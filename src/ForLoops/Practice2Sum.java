package ForLoops;

public class Practice2Sum {
    public static void main(String[] args) {


        for (int a=3; a>0; a--) {
            System.out.println("For loop");

        }
        //find a sum of the number between 10 and 15

        int sum=0;
        for (int b=10;b<=15; b++){
            sum+=b;
            System.out.println("The sum is " +sum);

        }//System.out.println("The sum is " +sum);// we can print out in the process and

        //count down to 0 from 20

        for (int f=20; f>=0; f--){

            System.out.println(f);//it will show the proccess
        }




    }

}
