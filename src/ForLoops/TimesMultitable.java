package ForLoops;

public class TimesMultitable {
    public static void main(String[] args) {
        /*
        create multiplication number for number from 1 to 10 inclusive
         */

        for (int i=1; i<=10; i++){
            for (int i1=1; i1<=10; i1++){
                System.out.println(i+"*"+ i1 + "=" + i*i1);
            }System.out.println("=========");

        }
    }
}
